import {PixelRatio} from 'react-native';

export const ml = (multiple = 1) => {
  return {
    marginLeft: 4 * multiple,
  };
};

export const mr = (multiple = 1) => {
  return {
    marginRight: 4 * multiple,
  };
};
export const mt = (multiple = 1) => {
  return {
    marginTop: 4 * multiple,
  };
};
export const mb = (multiple = 1) => {
  return {
    marginBottom: 4 * multiple,
  };
};
export const px2dp = (px: number) => PixelRatio.roundToNearestPixel(px);
